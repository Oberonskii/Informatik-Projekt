const LEARNHUB_SUPPORTED_LOCALES = ['de', 'en'];

function learnhub_normalize_locale(?string $locale): string
{
    $locale = strtolower(trim((string) $locale));
    return in_array($locale, LEARNHUB_SUPPORTED_LOCALES, true) ? $locale : 'de';
}

function learnhub_get_locale(): string
{
    if (isset($_SESSION['locale'])) {
        $_SESSION['locale'] = learnhub_normalize_locale($_SESSION['locale']);
        return $_SESSION['locale'];
    }

    $_SESSION['locale'] = 'de';
    return $_SESSION['locale'];
}

function learnhub_set_locale(?string $locale): string
{
    $_SESSION['locale'] = learnhub_normalize_locale($locale);
    return $_SESSION['locale'];
}

